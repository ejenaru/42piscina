int	ft_is_prime(int nb)
{
	int i;

	if (nb == 0 || nb == 1)
		return (0);
	else if (nb < 0)
		return (0);
	else
	{
		i = 2;
		while (i <= nb / 2)
		{
			if (nb % i == 0)
				return (0);
			i = i % 2 == 0 ? i + 1 : i + 2;
		}
		return (1);
	}
}
