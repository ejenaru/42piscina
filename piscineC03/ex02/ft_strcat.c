char *ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i >= 0)
	{
		if (dest[0] != 0)
			i++;
		else
		{
			dest[i] = src[j];
			if (src[j] == 0)
			{
				dest[i] = src[j];
				return (dest);
			}
			i++;
			j++;
		}

	}
	return (dest);
}
